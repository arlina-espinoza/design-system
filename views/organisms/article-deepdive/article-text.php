            <p class="text-block">
                For fostering Peace and Social Cohesion in Pujehun and Moyamba districts, communities score the Government of Sierra Leone,
                the United Nations Development Programme (UNDP) and the World Food Programme (WFP) over 80% in the Multi-Stakeholder Project
                supported by the Peacebuilding Fund (PBF).</p>
            <p class="text-block">
                Residents (youth, women, chiefs and other district and chiefdom authorities) of Malen and Makpele chiefdoms of Pujehun District
                and of Lower Banta and Upper Banta Chiefdoms in Moyamba district recently rated the Government of Sierra Leone (GoSL), UNDP and
                WFP Multi Stakeholder Platform (MSP) Project “over 80% successful” as a resource based and local conflict resolution mechanism
                that promotes peace and social cohesion. This finding was revealed by district stakeholders during a two-day spot-check visit to
                the project chiefdoms led by UNDP’s Resident Representative-Dr. Samuel Doe, where his team engaged with community members, local
                and traditional leaders of the areas to learn about the outcomes of <a href="#">The project was launched at the end of 2019</a>
            </p>
            <h3 class="heading h3">Some achievements of the project include the following:</h3>
            <ul>
                <li>
                    Helping to ensure land acquisition and land use processes are more inclusive and aligned with policy frameworks. A
                    Land Degradation Assessment completed by the Sierra Leone Agricultural Research Institute (SLARI) across the targeted chiefdoms
                    and districts indicates significant forest degradation informed by indigenous perceptions in the four chiefdoms and backed by
                    empirical evidence obtained from Landsat imagery analysed at 2000 and 2020. Forest loss is mainly attributed to the combined
                    influence of local activities (subsistence farming, lumbering, and charcoal production); mining (in the case of Lower and Upper Banta);
                    and expansions in oil palm plantations (more intense in Malen than in Mapkele). Indigenous perceptions suggest that the land tenure systems
                    are highly monopolized by local authorities and compensations or surface rents are grossly inadequate to provide sustainable and alternative
                    livelihoods after land dispossession. The attendant surplus labour, poverty and food insecurity have been major triggers of dissatisfaction
                    and conflict in these communities. Moreover, Chemical analysis of water samples proved that water bodies in mining localities are more
                    polluted than those in non-mining communities. Results from Malen showed that soil and water samples had higher pollutants than those in Makpele.
                </li>
            </ul>
